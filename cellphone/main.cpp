int main(int argc, char* argv[])
{
    string sDirName = "D:/watrix/2018-03-05同志科技数据/";

    for(int i = 0; i < 309; i++)
    {
        string fInput = sDirName + sFileNames[i];
        string fOutput = fInput + ".png";

        cout << i << ":" << fInput << endl;

        Mat im = imread(fInput);

        if( im.empty())
        {
            cerr << "Cannot open image file: " << fInput << endl;
            return -1;
        }

        double t;

        t = (double)cvGetTickCount();
        float fWidth = 1136.0f;
        float fHeight = 539.0f;
        float fRadius = 12.0f;
        float fHoleOffsetX = -540;
        float fHoleOffsetY = 121;
        float fHoleSearchRegion = 50;

        ScreenObjectInfo soi = findScreenObject(im, fWidth, fHeight, fRadius, fHoleOffsetX, fHoleOffsetY, fHoleSearchRegion);
        t = (double)cvGetTickCount() - t;
        t = t / ((double)cvGetTickFrequency()*1000.);
        cout << "time " << t << "ms" << endl;

        {//draw the results on the image and show on a window
            Mat draw = im.clone();
            circle(draw, Point(cvRound(soi.hole_x), cvRound(soi.hole_y)), 2, CV_RGB(0, 255, 0), 3, 8, 0);
            circle(draw, Point(cvRound(soi.center_x), cvRound(soi.center_y)), 25, CV_RGB(0, 255, 0), 1, 8, 0);
            ellipse(draw, Point(cvRound(soi.center_x), cvRound(soi.center_y)), Size(cvRound(fWidth / 2), cvRound(fHeight / 2)), soi.angle, 160, 360 + 150, CV_RGB(0, 255, 0), 1);

            //draw possibility
            char sP[1024];
            sprintf_s(sP, "score=%.2f", soi.possibility);
            putText(draw, sP, Point(cvRound(soi.center_x), cvRound(soi.center_y - 20)), FONT_HERSHEY_SIMPLEX, 1, Scalar(255, 255, 255), 2);

            imshow("result", draw);
            waitKey(0);
        }
        
    }
    return 0;
}
